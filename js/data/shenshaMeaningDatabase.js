export function inferPillarKey(pillarText = "") {
  const text = String(pillarText);

  if (text.includes("年柱")) return "year";
  if (text.includes("月柱")) return "month";
  if (text.includes("日柱")) return "day";
  if (text.includes("时柱")) return "hour";

  return "";
}

export function getShenshaMeaning(name, pillarKey = "") {
  const source = shenshaMeaningDatabase[name] ?? {};

  const definition =
    source.definition ??
    "这是传统命理中的辅助神煞，用于补充观察命局某一方面的倾向。";

  const manifestations = Array.isArray(source.manifestations)
    ? source.manifestations
    : [];

  const pillarMeaning =
    source.pillarMeanings?.[pillarKey] ??
    commonPillarMeanings[pillarKey] ??
    "";

  return {
    name,
    aliases: Array.isArray(source.aliases)
      ? source.aliases
      : [],
    definition,
    manifestations,
    pillarMeaning,
    caution:
      source.caution ??
      "神煞不能单独定论，需要结合十神、柱位、原局结构与岁运判断。",
  };
}